// Time-block planner: a `plannerBlocks` entity covers both the recurring
// weekly skeleton (sleep, meals, hygiene — `recurring: true` + `days`) and
// one-off blocks for a specific date (`recurring: false` + `date`), manual
// or auto-arranged. Auto-arrange is a pure, deterministic bin-packer — no
// AI — that slots the day's Momentum commitments into whatever's left after
// the fixed routine, same "work from data you already entered" philosophy
// as the rest of the app.
